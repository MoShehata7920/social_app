  void createPost({
    required String dateTime,
    required String text,
    String? postImage,
  }) {
    emit(SocialCreatePostLoadingState());
    // emit(SocialUserUpdateLoadingState());
    PostModel model = PostModel(
      name: userModel!.name,
      image: userModel!.image,
      uId: userModel!.uId,
      dateTime: dateTime,
      text: text,
      postImages: postImage ?? '',
    );

    FirebaseFirestore.instance
        .collection('posts')
        .add(model.toMap())
        .then((value) {
      emit(SocialCreatePostSuccessState());
    }).catchError((error) {
      emit(SocialCreatePostErrorState());
    });
  }

  List<PostModel> posts = [];
  //the likes list in firebase
  List<String> postId = [];
  //to count likes
  List<int> likes = [];
  // to count comments
  List<int> commentsNum = [];
  //to add comments

  void getPosts() {
    FirebaseFirestore.instance.collection('posts').get().then((value) {
      // ignore: avoid_function_literals_in_foreach_calls
      value.docs.forEach(
        (element) {
          element.reference.collection('likes').get().then((value) {
            //to count likes in the collection
            likes.add(value.docs.length);
            postId.add(element.id);
            posts.add(PostModel.fromJson(element.data()));
            emit(SocialGetPostsSuccessState());
          }).catchError((error) {});
          element.reference.collection('comments').get().then((value) {
            commentsNum.add(value.docs.length);
            postId.add(element.id);
            emit(SocialGetPostsSuccessState());
          }).catchError((error) {});
        },
      );
    }).catchError((error) {
      emit(SocialGetPostsErrorState(error.toString()));
    });
  }

  void postLikes(String postId) {
    FirebaseFirestore.instance
        .collection('posts')
        .doc(postId)
        .collection('likes')
        .doc(userModel!.uId)
        .set({
      'like': true,
    }).then((value) {
      emit(SocialLikePostSuccessState());
    }).catchError((error) {
      emit(SocialLikePostErrorState(error.toString()));
    });
  }

  CommentModel? comment;
  void createComment({
    String? dateTime,
    String? text,
    String? postId,
  }) {
    emit(SocialSendCommentLoadingState());
    // emit(SocialUserUpdateLoadingState());
    CommentModel comment = CommentModel(
        dateTime: dateTime,
        uId: userModel!.uId,
        comment: text,
        image: userModel!.image,
        name: userModel!.name);
    FirebaseFirestore.instance
        .collection('posts')
        .doc(postId)
        .collection('comments')
        .add(comment.toMap())
        .then((value) {
      emit(SocialSendCommentSuccessState());
    }).catchError((error) {
      emit(SocialSendCommentErrorState());
    });
  }

  List<CommentModel> comments = [];
  void getComments(String? postId) {
    FirebaseFirestore.instance
        .collection('posts')
        .doc(postId)
        .collection('comments')
        .get()
        .then((value) {
      // ignore: avoid_function_literals_in_foreach_calls
      value.docs.forEach(
        (element) {
          comments.add(CommentModel.fromJson(element.data()));
        },
      );
      emit(SocialGetCommentsSuccessState());
    }).catchError((error) {
      emit(SocialGetCommentsErrorState(error.toString()));
    });
  }

  // get all users for chat screen
  List<SocialUserModel> users = [];
  void getUsers() {
    if (users.isEmpty) {
      FirebaseFirestore.instance.collection('users').get().then((value) {
        // ignore: avoid_function_literals_in_foreach_calls
        value.docs.forEach(
          (element) {
            if (element.data()['uId'] != userModel!.uId) {
              users.add(SocialUserModel.fromJson(element.data()));
            }
          },
        );
        emit(SocialGetAllUsersSuccessState());
      }).catchError((error) {
        emit(SocialGetAllUsersErrorState(error.toString()));
      });
    }
  }

  // send messages
  MessageModel? messageModel;
  void sendMessages({
    required String receiverId,
    required String dateTime,
    String? text,
  }) {
    MessageModel model = MessageModel(
      text: text,
      senderId: userModel!.uId,
      receiverId: receiverId,
      dateTime: dateTime,
    );

    //set my chats
    FirebaseFirestore.instance
        .collection('users')
        .doc(userModel!.uId)
        .collection('chats')
        .doc(receiverId)
        .collection('messages')
        .add(model.toMap())
        .then((value) {
      emit(SocialSendMessageSuccessState());
    }).catchError((error) {
      emit(SocialSendMessageErrorState());
    });

    // set receiver chat
    FirebaseFirestore.instance
        .collection('users')
        .doc(receiverId)
        .collection('chats')
        .doc(userModel!.uId)
        .collection('messages')
        .add(model.toMap())
        .then((value) {
      emit(SocialSendMessageSuccessState());
    }).catchError((error) {
      emit(SocialSendMessageErrorState());
    });
  }

  List<MessageModel> messages = [];
  void getMessages({
    required String receiverId,
  }) {
    FirebaseFirestore.instance
        .collection('users')
        .doc(userModel!.uId)
        .collection('chats')
        .doc(receiverId)
        .collection('messages')
        .orderBy('dateTime')
        .snapshots()
        .listen((event) {
      messages = [];
      for (var element in event.docs) {
        messages.add(MessageModel.fromJson(element.data()));
      }
      emit(SocialGetMessageSuccessState());
    });
  }
